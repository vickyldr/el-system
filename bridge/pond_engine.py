def _pick(r, arr):
    return arr[r.randint(0, len(arr) - 1)]


def _pick_t(state, arr):
    """按回合数确定性取一条文案（不消耗 PRNG，保持随机流对齐）。"""
    return arr[state["turn"] % len(arr)]


def _ambient_present(state, name):
    """observe 文案物种校验：种群存在（含冬眠）或作为定居者在场。"""
    return _is_present(state, name) or _has_settler(state, name)


def _pick_ambient(state, arr):
    """从带物种依赖标注的 observe 文案池里确定性取一条。

    arr 中每项为 str（无依赖）或 (str, [依赖物种...])。过滤掉依赖物种当前
    不存在的文案，避免描写并不存在的生物。全被过滤则返回 None（调用方降级）。
    """
    avail = []
    for item in arr:
        if isinstance(item, tuple):
            text, deps = item
        else:
            text, deps = item, ()
        if all(_ambient_present(state, s) for s in deps):
            avail.append(text)
    if not avail:
        return None
    return avail[state["turn"] % len(avail)]


def _pick_idx_avoid(r, n, recent):
    """随机取一个不在 recent（最近用过）里的索引，避免短期重复。"""
    choices = [i for i in range(n) if i not in recent]
    if not choices:
        choices = list(range(n))
    return choices[r.randint(0, len(choices) - 1)]


# 连续完整描写后的降格简短文案
SHORT_HUNT = {
    "hit": "%s照常捕到了一条鱼。",
    "miss": "%s照常扑了个空。",
    "absent": "%s在水边守了一阵，没有出手。",
}


def _hunt_emit(state, s, events, r, arr, kind):
    """发出一条捕食描写：最近 3 条不重复；连续完整描写 3 天后降格/省略（item 8）。

    kind: 'hit' 用 settler_hunt 标签，'miss'/'absent' 用 settler_miss 标签。
    """
    tag = "settler_hunt" if kind == "hit" else "settler_miss"
    if s.get("desc_streak", 0) >= 3:
        # 连续完整描写已达 3 天：降格为简短一句，偶数回合干脆不出现在文字里
        if state["turn"] % 2 == 0:
            return
        events.append("%s:%s" % (tag, SHORT_HUNT[kind] % s["name"]))
        return
    recent = s.setdefault("recent_%s" % kind, [])
    idx = _pick_idx_avoid(r, len(arr), recent)
    recent.append(idx)
    if len(recent) > 3:
        recent.pop(0)
    events.append("%s:%s" % (tag, arr[idx]))
    s["desc_streak"] = s.get("desc_streak", 0) + 1


def _settler_hunt(state, s, hunter, events, r):
    """捕食型定居者（翠鸟/苍鹭）：概率捕食机制（item 13）。

    每天 40% 安静（不出现在描写）、60% 触发捕食尝试；尝试成功率 40%
    （猎物总数 < 5 时降为 20%；冬季翠鸟额外压到 15%），失败 60%。成功扣 1
    猎物 + health +0.1；连续 3 天没成功捕食开始掉血：第 3~5 天每天 -0.05，
    第 6 天起每天 -0.1。
    """
    pop = state["populations"]
    hit_arr, miss_arr, absent_arr = SETTLER_HUNT_TEXT.get(
        s["name"], (KINGFISHER_HIT, KINGFISHER_MISS, KINGFISHER_MISS))
    prey_list = hunter["prey"]
    if not r.chance(0.6):
        # 安静的一天：不出现在描写中（同时中断"连续完整描写"计数）
        s["since_hunt"] = s.get("since_hunt", 0) + 1
        s["desc_streak"] = 0
    else:
        prey_total = sum(pop.get(p, 0) for p in prey_list)
        if prey_total < 1:
            # 目标不存在 —— 扑空（专属文案）
            _hunt_emit(state, s, events, r, absent_arr, "absent")
            s["since_hunt"] = s.get("since_hunt", 0) + 1
        else:
            succ_p = 0.4 if prey_total >= 5 else 0.2
            # 冬季鱼活动迟缓更难捕捉：翠鸟成功率额外压到 15%（item 11）
            if state["season"] == "冬" and s["name"] == "翠鸟":
                succ_p = min(succ_p, 0.15)
            if r.chance(succ_p):
                for p in prey_list:
                    if pop.get(p, 0) >= 1:
                        pop[p] = max(0.0, pop[p] - 1)
                        break
                s["health"] = round(min(1.0, s["health"] + 0.1), 3)
                s["since_hunt"] = 0
                _hunt_emit(state, s, events, r, hit_arr, "hit")
            else:
                _hunt_emit(state, s, events, r, miss_arr, "miss")
                s["since_hunt"] = s.get("since_hunt", 0) + 1
    # 连续 3 天没成功捕食才开始掉血：第 3~5 天每天 -0.05，第 6 天起每天 -0.1。
    # 约 10~14 天彻底没食物就会饿到离开（item: 翠鸟健康下降加速）。
    sh = s.get("since_hunt", 0)
    if sh >= 6:
        s["health"] = round(s["health"] - 0.1, 3)
    elif sh >= 3:
        s["health"] = round(s["health"] - 0.05, 3)


def _consume_food(state, s, food):
    """杂食定居者按 daily_food 取食（item 8）：满足任一种即可进食，不会因缺一种就饿死。

    逐项尽量取食（不足则取尽），按"满足比例"折算 health 恢复（最多 +0.1）；
    只有当全部食物都一无所获时，才算彻底挨饿，health -0.1。
    """
    pop = state["populations"]
    env = state["env"]
    items = [(f, amt) for f, amt in food.items() if amt > 0]
    if not items:
        return
    satisfied = 0.0
    for f, amt in items:
        if f == "有机碎屑":
            have = env["detritus"]
            take = min(have, amt)
            env["detritus"] = max(0.0, have - take)
        else:
            have = pop.get(f, 0)
            take = min(have, amt)
            pop[f] = max(0.0, have - take)
        satisfied += take / amt
    ratio = satisfied / len(items)
    if ratio <= 0:
        # 所有食物都找不到：挨饿掉血
        s["health"] = round(s["health"] - 0.1, 3)
    else:
        # 吃到东西：按满足比例恢复体力（缺一两种不致命，只是恢复得少些）
        s["health"] = round(min(1.0, s["health"] + 0.1 * ratio), 3)


def _settler_graze(state, s, events, r):
    """非捕食型定居者（乌龟/螃蟹/水蛇/野鸭）：按 daily_food 取食，缺食扣 health。"""
    _consume_food(state, s, s["daily_food"])


def _settler_warn_chronicle(state, s):
    """食物不足预警：跨过 0.5 / 0.3 阈值时各记一次 chronicle（去重，恢复后可再触发）。"""
    h = s["health"]
    name = s["name"]
    lvl = s.get("warn_level", 0)
    if h < 0.3 and lvl < 2:
        s["warn_level"] = 2
        _chronicle(state, SETTLER_WARN_CHRON_HEAVY % name, key=False)
    elif 0.3 <= h < 0.5 and lvl < 1:
        s["warn_level"] = 1
        _chronicle(state, SETTLER_WARN_CHRON_LIGHT % name, key=False)
    elif h >= 0.5 and lvl != 0:
        s["warn_level"] = 0


def _process_settlers(state, events, r):
    """定居者每日摄食 + 幼体成长 + 衰老 + 繁殖（在种群 LV 计算之后）。"""
    env = state["env"]
    season = state["season"]
    survivors = []
    for s in state["settlers"]:
        s["age"] += 1
        name = s["name"]
        cfg = SETTLER_TYPES.get(name, {})
        # 变温动物冬眠：流浪乌龟冬季缩进淤泥，不摄食 / 不掉血 / 不离开，春季苏醒（item 7）
        if season == "冬" and name == "流浪乌龟":
            if not s.get("hibernating"):
                s["hibernating"] = True
                events.append("settler:乌龟缩进壳里，慢慢沉到水底的淤泥中。"
                              "它不吃也不动，把整个冬天睡了过去。")
                _chronicle(state, "乌龟潜入水底淤泥，开始冬眠。")
            rec = state["folio"]["settlers"].setdefault(name, {"times": 0, "max_days": 0})
            if s["age"] > rec.get("max_days", 0):
                rec["max_days"] = s["age"]
            survivors.append(s)
            continue
        # 冬眠结束：非冬季时苏醒（变温动物随水温回升重新活动）
        if s.get("hibernating") and season != "冬":
            s["hibernating"] = False
            events.append("settler:乌龟从淤泥里探出头来，壳上还沾着泥。"
                          "它慢吞吞爬向有阳光的浅水，冬眠结束了。")
            _chronicle(state, "乌龟结束冬眠，重新回到池塘。")
        # 幼体成长：到期转为独立定居者
        if s.get("juvenile"):
            s["juvenile_left"] = s.get("juvenile_left", cfg.get("juvenile_days", 0)) - 1
            if s["juvenile_left"] <= 0:
                s["juvenile"] = False
                grown = SETTLER_GROWN.get(name)
                if grown:
                    pool = grown
                    # 翠鸟幼体"叼着鲫鱼"文案需池塘有鱼，否则改用其他成长文案（补#6）
                    if name == "翠鸟" and state["populations"].get("鲫鱼", 0) < 1:
                        pool = [t for t in grown if "鲫鱼" not in t] or grown
                    events.append("settler_grown:" + _pick(r, pool))
                _chronicle(state, SETTLER_GROWN_CHRON % name)
        # 摄食
        if s.get("juvenile"):
            # 幼体不自己捕食，靠父母多养一张嘴：额外消耗一份口粮
            _consume_food(state, s, cfg.get("brood_food") or cfg.get("daily_food", {}))
        elif season == "冬" and name == "水蛇":
            # 冬季变温动物：水蛇不捕食，寒冷消耗 health 每天 -0.02（item 五）
            s["health"] = round(s["health"] - 0.02, 3)
        elif cfg.get("hunter"):
            _settler_hunt(state, s, cfg["hunter"], events, r)
        else:
            _settler_graze(state, s, events, r)
        # 定居者对环境的持续影响
        tpd = cfg.get("turbidity_per_day")
        if tpd:
            env["turbidity"] = _clamp(env["turbidity"] + tpd, 0.0, 1.0)
        if cfg.get("detritus_per_day"):
            env["detritus"] += cfg["detritus_per_day"]
        if cfg.get("nutrients_per_day"):
            env["nutrients"] += cfg["nutrients_per_day"]
        # 食物不足预警（chronicle 去重）
        _settler_warn_chronicle(state, s)
        # 每日更新万物志最长存活记录（item 6）
        rec = state["folio"]["settlers"].setdefault(name, {"times": 0, "max_days": 0})
        if s["age"] > rec.get("max_days", 0):
            rec["max_days"] = s["age"]
        # 离开判定
        txt = SETTLER_TEXT.get(name, {})
        leave = leave_chron = None
        if s["health"] <= 0:
            leave = txt.get("starve", "%s 找不到食物，离开了池塘。" % name)
            leave_chron = txt.get("starve_chron", leave)
        elif s["age"] > s["max_age"]:
            leave = txt.get("age", "年迈的%s悄然离去。" % name)
            leave_chron = txt.get("age_chron", leave)
        if leave:
            events.append("settler_leave:" + leave)
            _chronicle(state, leave_chron)
            # 个体分开计数：定居者离开/死亡后，对应访客来访次数清零，
            # 下一只同种访客从第 1 次重新累计（item 1）
            v = state["folio"]["visitors"].get(name)
            if v:
                v["count"] = 0
        else:
            survivors.append(s)
    # 繁殖：同种 >= 2，春夏季每天 5% 概率，未达上限则添一只幼体
    if season in ("春", "夏"):
        counts = {}
        for s in survivors:
            counts[s["name"]] = counts.get(s["name"], 0) + 1
        for name, cnt in list(counts.items()):
            cap = SETTLER_TYPES.get(name, {}).get("max_count", 99)
            if cnt >= 2 and cnt < cap and r.chance(0.05):
                survivors.append(_new_settler_dict(name, juvenile=True))
                bred = SETTLER_BREED.get(name)
                if bred:
                    events.append("settler_birth:" + _pick(r, bred))
                _chronicle(state, SETTLER_BREED_CHRON % name)
                _unlock(state, events, "池塘二代")
    state["settlers"] = survivors


def _choice_prompt(pc):
    c = pc["choices"]
    opts = " 或 ".join("choose %d (%s)" % (i + 1, lab) for i, lab in enumerate(c))
    return "%s\n请输入 %s" % (pc["desc"], opts)


def _parse_choice(args, choices):
    """正则冗余解析：choose 1 / choose 收留 / 1 / 收留 / 收留它 都识别为对应选项。"""
    s = "".join(a.strip() for a in args).strip() if args else ""
    if not s:
        return None
    m = re.match(r"^([0-9]+)$", s)
    if m:
        i = int(m.group(1))
        return i if 1 <= i <= len(choices) else None
    for i, c in enumerate(choices, 1):
        if s == c or c.startswith(s) or s in c or c[:2] == s[:2]:
            return i
    return None


def _choice_ready(state, key):
    """决策事件是否可触发：全局冷却 + 同类冷却 + 触发前提（目标物种存在）满足。"""
    spec = CHOICE_EVENTS[key]
    cds = state.get("choice_cooldowns", {})
    # 跨类型全局冷却：任意决策结算后 N 天内不触发任何新决策
    if state["turn"] - cds.get("__any__", -9999) < CHOICE_GLOBAL_COOLDOWN:
        return False
    # 同类冷却
    if state["turn"] - cds.get(key, -9999) < CHOICE_COOLDOWN:
        return False
    req = spec.get("requires") or []
    if req and not any(state["populations"].get(n, 0) >= 1 for n in req):
        return False
    return True


_CN_DIGITS = "零一二三四五六七八九"


def _cn_num(n):
    """把 1~99 的整数转成中文数字（用于"第N次"叙事），超出范围回退为阿拉伯数字。"""
    n = int(n)
    if n < 0 or n > 99:
        return str(n)
    if n < 10:
        return _CN_DIGITS[n]
    tens, ones = divmod(n, 10)
    s = ("" if tens == 1 else _CN_DIGITS[tens]) + "十"
    if ones:
        s += _CN_DIGITS[ones]
    return s


def _trigger_choice(state, events, key, desc_override=None):
    """触发一个决策事件：设置 pending_choice，本回合不再生成其他随机事件。

    desc_override：调用方传入的动态描述（如翠鸟"第N次"按真实来访次数生成）。
    """
    spec = CHOICE_EVENTS[key]
    # 动态描述：只提及当前真实在场的目标，避免"盯上不存在的猎物"
    desc = desc_override or spec["desc"]
    if desc_override is None and spec.get("desc_tmpl") and spec.get("requires"):
        present = [n for n in spec["requires"] if state["populations"].get(n, 0) >= 1]
        if present:
            desc = spec["desc_tmpl"] % "和".join(present)
    state["pending_choice"] = {
        "event": key,
        "desc": desc,
        "choices": list(spec["choices"]),
    }
    state.setdefault("choice_cooldowns", {})[key] = state["turn"]
    events.append("choice:" + desc)
    return True


