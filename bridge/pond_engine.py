def _disease_adjacent(state, name):
    """食物链相邻物种：捕食者（食谱含 name）+ 同层竞争者（共享食物来源）。"""
    sp = SPECIES[name]
    my_food = set(f for f in sp["food_sources"] if f != "飞虫")
    adj = set()
    for other, osp in SPECIES.items():
        if other == name:
            continue
        if name in osp["food_sources"]:
            adj.add(other)                      # 捕食者
        elif my_food & set(f for f in osp["food_sources"] if f != "飞虫"):
            adj.add(other)                      # 同层竞争者
    return [a for a in adj if a in RESIDENT_SPECIES]


def _start_disease(state, events, name, r):
    state["diseases"][name] = {"elapsed": 0, "duration": r.randint(7, 10)}
    _chronicle(state, _pick(r, DISEASE_CHRON_START))
    _unlock(state, events, "疫病爆发")


def _process_disease(state, events, r):
    """逐日推进疫病：前兆(1-2)只描写、扩散(3-5)death×2且30%传染、高峰(6+)death×3。"""
    pop = state["populations"]
    env = state["env"]
    diseases = state.setdefault("diseases", {})
    immune = state.setdefault("disease_immune", {})
    turn = state["turn"]
    ended = []
    newly = []
    for name in list(diseases.keys()):
        d = diseases[name]
        d["elapsed"] += 1
        day = d["elapsed"]
        sp = SPECIES[name]
        if day <= 2:
            phase, mult = "onset", 1.0
            if name in DISEASE_ONSET:
                txt = _pick(r, DISEASE_ONSET[name])
            else:
                txt = _pick(r, DISEASE_ONSET_GENERIC) % name
        elif day <= 5:
            phase, mult = "spread", 2.0
            txt = _pick(r, DISEASE_SPREAD)
        else:
            phase, mult = "peak", 3.0
            txt = _pick(r, DISEASE_PEAK)
        # 额外死亡（叠加在已结算的基础死亡之上）
        if mult > 1.0 and pop.get(name, 0) > 0:
            extra = pop[name] * sp["death_rate"] * (mult - 1.0)
            new_n = max(0.0, pop[name] - extra)
            env["detritus"] += (pop[name] - new_n) * 0.5
            pop[name] = new_n
        events.append("disease:" + txt)
        # 扩散期：30% 概率传染到食物链相邻的一个物种（clean 后传播概率减半）
        spread_p = 0.15 if _chain_active(state, "sanitized") else 0.3
        if phase == "spread" and r.chance(spread_p):
            cands = [a for a in _disease_adjacent(state, name)
                     if pop.get(a, 0) >= 1 and a not in diseases and turn > immune.get(a, -1)]
            if cands:
                target = cands[r.randint(0, len(cands) - 1)]
                newly.append(target)
                _chronicle(state, _pick(r, DISEASE_CHRON_SPREAD))
                events.append("disease:" + _pick(r, DISEASE_TRANSMIT))
        if day >= d["duration"]:
            ended.append(name)
    for name in ended:
        diseases.pop(name, None)
        immune[name] = turn + 30          # 结束后 30 天免疫
        _chronicle(state, _pick(r, DISEASE_CHRON_END))
        events.append("disease:" + _pick(r, DISEASE_END))
    for t in newly:
        diseases.setdefault(t, {"elapsed": 0, "duration": r.randint(7, 10)})
    # 触发新疫病：密度 > 80% 承载且非免疫，每天 3% 概率；一回合最多新发一种
    for name in RESIDENT_SPECIES:
        if name in diseases or turn <= immune.get(name, -1):
            continue
        if pop.get(name, 0) > 0.8 * SPECIES[name]["max_capacity"] and r.chance(0.03):
            _start_disease(state, events, name, r)
            break


def tick(state):
    """推进一回合，返回本回合事件文字列表。"""
    r = rng_from(state)
    events = []
    pop = state["populations"]
    env = state["env"]

    # 推进前快照：供本轮 delta（补一·4）与 status 趋势箭头（补一·1）
    _prev_pop = {n: pop.get(n, 0.0) for n in RESIDENT_SPECIES}
    state["prev_env"] = {
        "dissolved_oxygen": env["dissolved_oxygen"],
        "nutrients": env["nutrients"],
        "detritus": env["detritus"],
    }

    state["turn"] += 1
    turn = state["turn"]
    season = season_of(turn)
    prev_season = state["season"]
    state["season"] = season
    if season != prev_season:
        events.append("season:" + SEASON_ENV[season]["desc"])
        _chronicle(state, "入%s，%s" % (season, SEASON_ENV[season]["desc"]))
    # 季节切换的自动事件（融冰/抽芽/枯黄/昆虫冻死等）
    _season_events(state, events, season, prev_season)

    # --- (1) 环境参数变化 ---
    target = SEASON_ENV[season]
    # 水温向"季末 3 天渐变过渡"后的目标缓动，避免阶跃（item 六）
    env["water_temp"] += (_ramped_target_temp(turn) - env["water_temp"]) * 0.34
    # 冬季结冰：水温 < 4℃ 自动进入结冰状态，持续到春季（item 五）
    if season == "冬" and env["water_temp"] < 4.0:
        if not state["flags"].get("ice_on"):
            state["flags"]["ice_on"] = True
            events.append("weather:" + SEASON_TEXT["结冰"])
    ice = state["flags"].get("ice_on", False)
    # 水面覆盖率（浮萍 + 睡莲）影响光照
    duckweed_cover = _surface_cover(state)
    # 大雾：光照系数压低（连锁效果，持续约 2 天）；结冰再额外压低光照 ×0.3
    light_target = target["light"] * (0.3 if _chain_active(state, "fog") else 1.0) \
        * (0.3 if ice else 1.0)
    base_light = light_target * (1 - 0.6 * duckweed_cover) * (1 - 0.5 * env["turbidity"])
    env["light"] += (base_light - env["light"]) * 0.3
    env["light"] = _clamp(env["light"], 0.0, 1.0)
    # 浑浊度自然沉降
    env["turbidity"] = _clamp(env["turbidity"] * 0.9, 0.0, 1.0)
    # 秋季落叶：每天自动增加有机碎屑（item 四）
    if season == "秋":
        env["detritus"] += 0.5
    # 溶氧：温度越高溶氧越低；光合作用（水藻）产氧；覆盖>80%阻断气体交换
    do_sat = _clamp(14.6 - 0.35 * env["water_temp"], 4.0, 14.6)
    photo = 0.002 * pop["水藻"] * env["light"]
    exchange = 1.0 if duckweed_cover < 0.8 else 0.2
    env["dissolved_oxygen"] += ((do_sat - env["dissolved_oxygen"]) * 0.3 * exchange) + photo * 0.01
    # 结冰阻断气体交换：溶氧每天额外 -0.5（与浮萍覆盖叠加，item 五）
    if ice:
        env["dissolved_oxygen"] -= 0.5
    # 凿冰洞（crack）：洞口气体交换恢复，持续 3 天每天溶氧额外 +0.3
    if _chain_active(state, "ice_hole"):
        env["dissolved_oxygen"] += 0.3
    # 浮萍 / 睡莲覆盖收紧（item D4）：覆盖 70% 起溶氧每天 -0.3；
    # 90% 以上彻底封住水面，溶氧每天 -1.0 且光照压到 0.1。
    if duckweed_cover >= 0.9:
        env["dissolved_oxygen"] -= 1.0
        env["light"] = min(env["light"], 0.1)
    elif duckweed_cover >= 0.7:
        env["dissolved_oxygen"] -= 0.3
    env["dissolved_oxygen"] = _clamp(env["dissolved_oxygen"], 0.0, 16.0)

    # 冬季睡莲枯萎到 0（来年春天从根茎恢复，见 _season_events）
    if season == "冬":
        if pop.get("睡莲", 0) >= 1:
            state["flags"]["had_lily"] = True
            pop["睡莲"] = 0.0
        pop["蚊子"] = 0.0
        pop["蜻蜓成虫"] = 0.0
        # 青蛙冬眠：完全移出种群计算（不捕食/不被捕食/不死亡/不繁殖），暂存待春季恢复
        if pop.get("青蛙", 0) > 0:
            hib = state.setdefault("hibernate", {})
            hib["青蛙"] = hib.get("青蛙", 0.0) + pop["青蛙"]
            pop["青蛙"] = 0.0

    # --- (1b) 持续性灾害逐日演化（在自然恢复趋势之上叠加灾害施压） ---
    _process_weather(state, events)

    # --- (2) 生产者 Logistic 增长 ---
    temp_factor = _clamp(1 - abs(env["water_temp"] - 24) / 30, 0.2, 1.0)
    # 干旱期间水容量视为减半：所有物种 max_capacity 临时 ×0.5（item D2：加重干旱）
    drought_cap = 0.5 if _chain_active(state, "drought") else 1.0
    for name in ("水藻", "浮萍", "睡莲", "芦苇"):
        sp = SPECIES[name]
        n = pop[name]
        if n <= 0 and name in ("睡莲",):
            continue  # 未投放的睡莲不生长
        if name == "水藻":
            k = sp["max_capacity"] * env["light"] * _clamp(env["nutrients"] / 50, 0.1, 2.0)
        elif name == "浮萍":
            # 软化季节摆动：K 维持在 cap 的较高比例，避免覆盖率大起大落
            k = sp["max_capacity"] * (0.7 + 0.3 * _clamp(env["nutrients"] / 50, 0.0, 1.0)) \
                * (0.75 + 0.25 * target["light"])
        elif name == "睡莲":
            # 与浮萍类似的水面植物，受营养盐与季节光照影响，但更慢更稳
            k = sp["max_capacity"] * (0.6 + 0.4 * _clamp(env["nutrients"] / 50, 0.0, 1.0)) \
                * (0.7 + 0.3 * target["light"])
        else:  # 芦苇 受季节影响小
            k = sp["max_capacity"] * temp_factor
        k = max(1.0, k * drought_cap)
        sbf = _season_birth_factor(state, name, season)
        growth = sp["birth_rate"] * sbf * temp_factor * n * (1 - n / k)
        death = sp["death_rate"] * _season_death_factor(state, name, season, turn) * n
        pop[name] = max(0.0, n + growth - death)
        # 死亡→恶化连锁：水生植物枯死的生物量也沉为有机碎屑（item D1）
        if name in ("水藻", "浮萍", "睡莲") and death > 0:
            env["detritus"] += death * 0.2
        # 光合吸收：水生植物生长时消耗营养盐（item 8：营养盐不再只涨不降）。
        # 用 max(0, growth) 防止藻量超过承载上限（growth 为负）时反向"返还"营养盐。
        if name in ("水藻", "浮萍", "睡莲"):
            env["nutrients"] = max(0.0, env["nutrients"] - max(0.0, growth) * 0.05)
        # 芦苇也吸收营养盐（速率略低于水生植物）
        if name == "芦苇":
            env["nutrients"] = max(0.0, env["nutrients"] - max(0.0, growth) * 0.03)

    # --- (3) 捕食（Lotka-Volterra 简化） ---
    consumed_detritus = 0.0
    # 收集捕食意图后统一结算，避免顺序偏差
    deltas = {name: 0.0 for name in SPECIES}
    for name in SPECIES:
        sp = SPECIES[name]
        if not sp["food_sources"] and not sp.get("detritus_feeder"):
            continue
        n = pop[name]
        if n <= 0:
            continue
        eff = sp["predation"] * temp_factor
        food_eff = sp.get("food_efficiency", {})
        for prey in sp["food_sources"]:
            if prey not in pop:
                continue
            peff = eff * food_eff.get(prey, 1.0)
            # 苍鹭定居后，鲫鱼/泥鳅学会躲藏，被捕食系数永久 -20%
            if prey in ("鲫鱼", "泥鳅") and state["flags"].get("heron_resident"):
                peff *= 0.8
            eaten = _clamp(peff * n * pop[prey], 0, pop[prey] * 0.5)
            deltas[prey] -= eaten
            # 转化为捕食者增长
            deltas[name] += eaten * 0.15
        if sp.get("detritus_feeder"):
            d = _clamp(0.0005 * n * env["detritus"], 0, env["detritus"] * 0.3)
            consumed_detritus += d
            deltas[name] += d * 0.05
    env["detritus"] = max(0.0, env["detritus"] - consumed_detritus)
    for name, d in deltas.items():
        pop[name] = max(0.0, pop[name] + d)

    # --- (4) 消费者自然增长 + 自然死亡 ---
    # 同层竞争：先统计每种食物的总需求（各消费者当前种群之和），
    # 之后每个物种只能按自己种群占比拿到那份食物，而非独享全部。
    food_demand = {}
    for cname in SPECIES:
        csp = SPECIES[cname]
        if csp["trophic"] == "producer":
            continue
        cn = pop.get(cname, 0)
        if cn <= 0:
            continue
        for fsrc in csp["food_sources"]:
            if fsrc == "飞虫":
                continue
            food_demand[fsrc] = food_demand.get(fsrc, 0.0) + cn

    for name in SPECIES:
        sp = SPECIES[name]
        if sp["trophic"] == "producer":
            continue
        n = pop[name]
        if n <= 0:
            continue
        # 季节繁殖系数（鱼类冬季为 0、春季加成、夏季孑孓/水蚯蚓加成等，item 一~五）
        br = sp["birth_rate"] * _season_birth_factor(state, name, season) * temp_factor
        # 有食物来源者，繁殖受食物丰度影响：保底 20% 基础繁殖率，食物越多越接近满速
        if sp["food_sources"]:
            # "飞虫" 是隐形保底食物：只要有水草或有机碎屑，飞虫就存在，不计入真实种群
            # 同层竞争：共享食物按种群比例分配，本物种只拿到自己那份
            real_food = 0.0
            for f in sp["food_sources"]:
                if f == "飞虫":
                    continue
                dem = food_demand.get(f, 0.0)
                share = (n / dem) if dem > 0 else 0.0
                real_food += pop.get(f, 0) * share
            factor = 0.2 + 0.8 * _clamp(real_food / (sp["max_capacity"] * 0.5), 0.0, 1.0)
            if "飞虫" in sp["food_sources"]:
                flybugs = pop.get("水藻", 0) > 0 or pop.get("浮萍", 0) > 0 or env["detritus"] > 0
                if flybugs:
                    # 飞虫提供固定 0.3 的食物因子保底；成虫充足时正常计算会更高
                    factor = max(factor, 0.3)
            br *= factor
        # 孑孓等：有机碎屑越多繁殖越快
        if sp.get("detritus_breeder"):
            br *= 1.0 + _clamp(env["detritus"] / 100.0, 0.0, 1.5)
        # 淡水虾/水蚯蚓：碎屑超过阈值时繁殖率跳增
        bonus = sp.get("detritus_breed_bonus")
        if bonus and env["detritus"] > bonus["threshold"]:
            br *= bonus["mult"]
        # 睡莲在场：青蛙获得栖息加成，繁殖率 ×1.3
        if name == "青蛙" and pop.get("睡莲", 0) >= 1:
            br *= 1.3
        # 干旱期间：青蛙繁殖受抑，蟾蜍免疫
        if name == "青蛙" and _chain_active(state, "drought") and not sp.get("drought_immune"):
            br *= 0.85
        # 干旱期间蟾蜍繁殖 ×1.5
        if sp.get("drought_immune") and _chain_active(state, "drought"):
            br *= 1.5
        # 冬季青蛙休眠（繁殖减缓）；蟾蜍不休眠
        if season == "冬" and not sp.get("no_hibernate") and name in ("青蛙",):
            br *= 0.5
        # 水蚯蚓充足时泥鳅食物丰沛，繁殖 ×1.2
        if name == "泥鳅" and pop.get("水蚯蚓", 0) > 100:
            br *= 1.2
        k = sp["max_capacity"] * drought_cap   # 干旱水容量减半（item D2）
        births = br * n * (1 - n / max(1.0, k))
        deaths = sp["death_rate"] * _season_death_factor(state, name, season, turn) * n
        # 低溶氧致死
        if sp.get("needs_oxygen") and env["dissolved_oxygen"] < 4.0:
            deaths += n * (4.0 - env["dissolved_oxygen"]) / 4.0 * 0.5
        # 溶氧低于 3 时鱼类死亡率 ×3（鲫鱼 / 鲤鱼 / 泥鳅，item D2）
        if name in FISH and env["dissolved_oxygen"] < 3.0:
            deaths *= 3.0
        # 水黾：浮萍覆盖过高没有活动空间，死亡率翻倍
        cdp = sp.get("cover_death_penalty")
        if cdp and _surface_cover(state) > cdp["threshold"]:
            deaths *= cdp["mult"]
        # 青蛙干旱期间死亡率上升（蟾蜍免疫）
        if name == "青蛙" and _chain_active(state, "drought") and not sp.get("drought_immune"):
            deaths *= 1.5
        # 水蛇定居：田鼠不敢出洞，死亡率降低 50%
        if name == "田鼠" and _has_settler(state, "水蛇"):
            deaths *= 0.5
        # 冬季青蛙休眠：代谢降低，死亡率随繁殖一同减缓（蛰伏越冬）
        if season == "冬" and name == "青蛙":
            deaths *= 0.5
        new_n = max(0.0, n + births - deaths)
        # 死亡转化为有机碎屑
        env["detritus"] += (n - new_n) * 0.5 if new_n < n else 0.0
        pop[name] = new_n

    # --- (4b) 物种特殊效果（河蚌滤食 / 水蚯蚓分解 / 鲤鱼翻泥等） ---
    _apply_special_effects(state, events, r)

    # --- (4c) 疾病系统（密度过高触发，逐期加重，可沿食物链传染） ---
    _process_disease(state, events, r)

    # --- (5) 生命周期变态 ---
    _process_lifecycle(state, events, r)
    _process_spawning(state, events, season)

    # --- (6) 细菌分解（消耗溶氧，碎屑→营养盐） ---
    bac = pop["细菌"]
    sp_b = SPECIES["细菌"]
    # 细菌繁殖受碎屑驱动
    food_factor = _clamp(env["detritus"] / 100, 0.0, 2.0)
    bac_k = sp_b["max_capacity"] * food_factor
    bac = bac + sp_b["birth_rate"] * bac * (1 - bac / max(1.0, bac_k)) - sp_b["death_rate"] * bac
    # 细菌是水体自带的基础微生物群落：保留 >=1 的种子，碎屑出现时能重新繁盛，
    # 不会因一时缺乏有机质而彻底消失（否则乘性增长无法从 0 恢复）。
    bac = max(1.0, bac)
    decompose = _clamp(0.002 * bac * env["detritus"], 0, env["detritus"])
    o2_cost = decompose * 0.02
    env["detritus"] -= decompose
    env["nutrients"] += decompose * 0.4
    env["dissolved_oxygen"] = max(0.0, env["dissolved_oxygen"] - o2_cost)
    pop["细菌"] = bac
    # 翻塘判定：碎屑过多 + 细菌爆发 → 溶氧暴跌
    if env["dissolved_oxygen"] < 2.0 and env["detritus"] > 150:
        kill = 0.4
        for name in SPECIES:
            if SPECIES[name].get("needs_oxygen"):
                lost = pop[name] * kill
                pop[name] -= lost
                env["detritus"] += lost * 0.5
        events.append("crisis:水里像被抽走了呼吸，鱼纷纷浮起，白腹朝天。")
        _unlock(state, events, "翻塘")
    # 溶氧跌破 1.5：彻底翻塘——所有鱼类一次性 -50%（item D2）。
    # 去重：触发一次后置标记，溶氧回到 3 以上才重置，避免每天重复打断。
    if env["dissolved_oxygen"] < 1.5:
        if not state["flags"].get("turnover_alerted"):
            killed = 0.0
            for name in FISH:
                lost = pop.get(name, 0) * 0.5
                pop[name] = max(0.0, pop.get(name, 0) - lost)
                env["detritus"] += lost * 0.5
                killed += lost
            state["flags"]["turnover_alerted"] = True
            if killed >= 1:
                events.append("crisis:溶氧跌到了谷底，水面成片翻起白肚。"
                              "这一夜过后，池塘里的鱼几乎死绝了。")
                _unlock(state, events, "翻塘")
    elif env["dissolved_oxygen"] >= 3.0:
        state["flags"]["turnover_alerted"] = False

    # 营养盐缓慢自然流失 / 上限
    env["nutrients"] = _clamp(env["nutrients"], 0.0, 500.0)
    env["detritus"] = _clamp(env["detritus"], 0.0, 1000.0)

    # --- (6b) 定居者摄食 + 衰老（在种群层 LV 计算之后） ---
    _process_settlers(state, events, r)

    # --- (6c) 解锁制：刷新历史最大值与连续天数，检查物种解锁 ---
    _update_max_seen(state)
    _update_streaks(state)
    _check_discovery(state, events)
    _folio_update_species(state)

    # --- (6d) 归零物种自然恢复（生存条件满足时每天小概率重现） ---
    _natural_recovery(state, events, r)

    # --- (7) 随机事件（含决策事件） ---
    _random_events(state, events, r, season)

    # --- 取整种群（保留隐形精度，对外展示用整数）---
    # 内部保留浮点，方便长期演化

    # --- 关键事件检测（供 wait 暂停） ---
    _detect_pause(state, events)

    # --- 成就检测 ---
    _check_achievements(state, events)

    # --- 年终生态简报：每满一年（120 天）输出一段（item 28） ---
    if turn % YEAR_LEN == 0:
        _year_report(state, events)

    # --- 图鉴：记录出现过的物种 ---
    for name in RESIDENT_SPECIES:
        if pop[name] >= 1 and name not in state["seen"]:
            state["seen"].append(name)

    # --- 连续无归零计数（冬眠不算归零） ---
    extinct_now = any(not _is_present(state, n) for n in RESIDENT_SPECIES if n in state["seen"])
    if extinct_now:
        state["flags"]["days_no_extinction"] = 0
    else:
        state["flags"]["days_no_extinction"] += 1

    # 本轮各物种变化量（补一·4，只记非零）
    state["last_delta"] = {
        n: int(round(pop[n] - _prev_pop.get(n, 0.0)))
        for n in RESIDENT_SPECIES
        if int(round(pop[n] - _prev_pop.get(n, 0.0))) != 0
    }
    # 历史快照，最多保留 30 天（补一·7）
    hist = state.setdefault("history", [])
    hist.append({
        "turn": turn,
        "pop": {n: int(round(pop[n])) for n in RESIDENT_SPECIES if pop[n] >= 1},
        "DO": round(env["dissolved_oxygen"], 1),
        "nutrients": round(env["nutrients"], 0),
        "detritus": round(env["detritus"], 0),
    })
    if len(hist) > 30:
        del hist[:len(hist) - 30]

    state["log"] = events
    commit_rng(state, r)
    return events


def _has_settler(state, name):
    return any(s["name"] == name for s in state.get("settlers", []))


def _is_present(state, name):
    """物种是否仍存在：种群 >= 1，或正在冬眠（暂存数量 >= 1）。

    冬眠中的物种不应被判为归零（item 五：青蛙冬眠不是死亡）。
    """
    if state["populations"].get(name, 0) >= 1:
        return True
    return state.get("hibernate", {}).get(name, 0) >= 1


def _apply_special_effects(state, events, r):
    """V1.0 扩展物种的特殊环境效果（在 LV 计算之后结算）。"""
    pop = state["populations"]
    env = state["env"]
    # 翻底泥：含 turbidity_per_day 的物种在场则微量增加浑浊度
    for name in SPECIES:
        tpd = SPECIES[name].get("turbidity_per_day")
        if tpd and pop.get(name, 0) >= 1:
            env["turbidity"] = _clamp(env["turbidity"] + tpd, 0.0, 1.0)
    # 河蚌滤食：降浑浊、扣水蚤、产假粪；浑浊或缺氧时额外死亡
    m = pop.get("河蚌", 0)
    if m >= 1:
        scale = min(1.0, m / 10.0)
        env["turbidity"] = max(0.0, env["turbidity"] - 0.02 * scale)
        pop["水蚤"] = max(0.0, pop.get("水蚤", 0) - (1.0 + r.random()) * scale)
        env["detritus"] += 0.5 * scale
        env["nutrients"] = max(0.0, env["nutrients"] - 0.3 * scale)
        extra = 0.0
        if env["turbidity"] > 0.5:
            extra += m * SPECIES["河蚌"]["death_rate"]
        if env["dissolved_oxygen"] < 3.0:
            extra += m * SPECIES["河蚌"]["death_rate"]
        if extra:
            pop["河蚌"] = max(0.0, m - extra)
    # 水蚯蚓：加速碎屑分解，转化为营养盐
    w = pop.get("水蚯蚓", 0)
    if w >= 1:
        dec = min(env["detritus"], 1.0 * min(1.0, w / 100.0))
        env["detritus"] -= dec
        env["nutrients"] += dec * 0.5


def _update_streaks(state):
    """更新解锁制/成就所需的连续天数与历史标记。"""
    f = state["flags"]
    pop = state["populations"]
    env = state["env"]
    if env["turbidity"] < 0.2 and env["dissolved_oxygen"] > 4.0:
        f["clear_streak"] = f.get("clear_streak", 0) + 1
    else:
        f["clear_streak"] = 0
    if env["turbidity"] < 0.15:
        f["clear_ever"] = True
    if pop.get("浮萍", 0) > 0 and pop.get("水藻", 0) > 0:
        f["lily_streak"] = f.get("lily_streak", 0) + 1
    else:
        f["lily_streak"] = 0
    if pop.get("睡莲", 0) > 0 and pop.get("水藻", 0) > 0:
        f["garden_streak"] = f.get("garden_streak", 0) + 1
    else:
        f["garden_streak"] = 0


def _process_lifecycle(state, events, r):
    """处理 cohort 年龄推进与变态。"""
    pop = state["populations"]
    season = state["season"]
    for name, cohorts in state["cohorts"].items():
        sp = SPECIES[name]
        lc = sp["lifecycle"]
        # 把当前未入队的种群数量同步进 cohort（新增的视为 age 0）
        tracked = sum(c[0] for c in cohorts)
        actual = pop[name]
        if actual > tracked + 0.5:
            cohorts.append([actual - tracked, 0])
        elif actual < tracked - 0.5:
            # 因捕食/死亡减少，按比例缩减各 cohort
            if tracked > 0:
                scale = max(0.0, actual / tracked)
                for c in cohorts:
                    c[0] *= scale
        # 推进年龄，处理成熟
        matured_total = 0.0
        new_cohorts = []
        for count, age in cohorts:
            age += 1
            if age >= lc["stage_days"]:
                # 季节限制（如蜻蜓幼虫仅夏末羽化）
                if lc.get("season_only") and season != lc["season_only"]:
                    new_cohorts.append([count, age])
                    continue
                into = lc["into"]
                matured = count * lc["ratio"]
                if into in pop:
                    pop[into] += matured
                matured_total += matured
                # 未成熟的部分死亡（不再保留）
            else:
                new_cohorts.append([count, age])
        state["cohorts"][name] = new_cohorts
        if matured_total >= 1:
            pop[name] = max(0.0, pop[name] - matured_total / max(lc["ratio"], 0.01))
            events.append("lifecycle:%d 只%s悄然变了形态，化作%s" % (int(matured_total), name, lc["into"]))


def _process_spawning(state, events, season):
    """青蛙春季产卵、蜻蜓成虫秋季产卵。"""
    pop = state["populations"]
    turn = state["turn"]
    # 青蛙产卵：春季，每季触发一次（季初）
    fr = SPECIES["青蛙"].get("spawns")
    if fr and season == fr["season"] and turn % SEASON_LEN == 1 and pop["青蛙"] >= 0.5:
        eggs = pop["青蛙"] * fr["ratio"]
        pop[fr["into"]] += eggs
        events.append("spawn:春水微暖，青蛙产下卵块。数日后，%d 只蝌蚪挤在浅水里。" % int(eggs))
    # 死锁解除：春季只要还有青蛙（哪怕被压得很低）却完全没有蝌蚪，立即补一批
    elif fr and season == fr["season"] and pop["青蛙"] >= 0.5 and pop[fr["into"]] < 1:
        eggs = pop["青蛙"] * 5
        pop[fr["into"]] += eggs
        events.append("spawn:春日的光下，青蛙寻得水域产卵，%d 只蝌蚪孵了出来。" % int(eggs))
    # 蝌蚪一旦产出（种群 > 0）即纳入名册与图鉴，使 status / folio 能显示其数量（item 2）
    if fr and pop.get(fr["into"], 0) >= 1:
        into = fr["into"]
        if into not in state["unlocked_species"]:
            state["unlocked_species"].append(into)
        if into not in state["seen"]:
            state["seen"].append(into)
    # 蜻蜓成虫秋季产卵
    asp = SPECIES["蜻蜓成虫"].get("autumn_spawn")
    if asp and season == asp["season"] and turn % SEASON_LEN == 1 and pop["蜻蜓成虫"] >= 1:
        larvae = pop["蜻蜓成虫"] * asp["ratio"]
        pop[asp["into"]] += larvae
        events.append("spawn:蜻蜓点过水面，卵沉入水底。不久，%d 只幼虫潜伏在淤泥里。" % int(larvae))


