def _cmd_summon(state, args):
    if not args:
        return "用法：summon [物种] [数量]"
    name = _resolve_species(args[0])
    qty = 10
    if len(args) >= 2 and args[1].lstrip("-").isdigit():
        qty = int(args[1])
        # 拒绝零与负数（item 10）
        if qty <= 0:
            return "数量必须大于零。"
    if name is None:
        # 不限制：未知物种，自然规律当裁判 —— 直接告知它无法在池塘存活
        return "你投下%s，它在水中闪烁一下，便消散了。" % args[0]
    # 解锁制：只能召唤已解锁物种
    if name not in state.get("unlocked_species", []):
        return "造物名册中尚无此物种的记录。"
    # 归零冷却：物种归零后 15 天内不能 summon 同种物种
    et = state.get("extinct_turn", {}).get(name)
    if et is not None and state["turn"] - et < 15:
        return ("这片水域还留着它们消失的痕迹，现在还不是重新引入的时候。"
                "（还需等待 %d 天）" % (15 - (state["turn"] - et)))
    new_pop = state["populations"].get(name, 0) + qty
    state["populations"][name] = new_pop
    if name not in state["seen"]:
        state["seen"].append(name)
    # 投放后的瞬时数量计入历史最大值，使大批量投放能立刻满足解锁制阈值
    ms = state["max_seen"]
    if new_pop > ms.get(name, 0):
        ms[name] = new_pop
    _mark_intervention(state, True)
    # 本次命令的事件列表：成就通知与新物种解锁都汇入这里，确保能渲染给玩家
    disc = []
    _unlock(state, disc, "初生之池")
    # 大量投放的生态冲击（item D5）：单次 > 50 个体，溶氧即时下降（每 10 个体 -0.1）、浑浊 +0.05
    shock = None
    if qty > 50:
        env = state["env"]
        env["dissolved_oxygen"] = max(0.0, env["dissolved_oxygen"] - 0.1 * (qty // 10))
        env["turbidity"] = _clamp(env["turbidity"] + 0.05, 0.0, 1.0)
        shock = "大量生灵涌入池塘，水面一阵骚动，水底的溶氧被搅得跌了一截。"
    # 立即检查是否因这次投放解锁了新物种
    _check_discovery(state, disc)
    lines = ["✋ 你向池塘投放了 %d 个「%s」（%s）。" % (qty, name, SPECIES[name]["space"])]
    if shock:
        lines.append(shock)
    lines.extend(_render_events(disc))
    return "\n".join(lines)


def _cmd_remove(state, args):
    if not args:
        return "用法：remove [物种] [数量]"
    # 先看是否为定居者 —— 对定居者为"驱离"
    target = args[0].strip()
    for s in list(state["settlers"]):
        if s["name"] == target or _resolve_species(target) == s["name"]:
            state["settlers"].remove(s)
            _mark_intervention(state, True)
            _chronicle(state, "你将%s送出池塘，它头也不回地走了。" % s["name"])
            return "🚪 你驱离了「%s」。它沿着岸边，慢慢走远，没有回头。" % s["name"]
    name = _resolve_species(args[0])
    if name is None or name not in state["populations"]:
        return "池塘里没有「%s」。" % args[0]
    cur = state["populations"][name]
    shown = _ipop(state, name)          # 玩家在面板上看到的整数量
    if shown <= 0:
        return "池塘里没有「%s」。" % args[0]
    # 按整数量执行并显示，避免"想移除 2 个却显示移除 1 个"（item 9）
    if len(args) >= 2 and args[1].lstrip("-").isdigit():
        qty = min(shown, max(0, int(args[1])))
    else:
        qty = shown
    if qty <= 0:
        return "移除数量必须大于零。"
    # 扣满显示量即清零；否则从当前种群里扣掉整数 qty
    state["populations"][name] = 0.0 if qty >= shown else max(0.0, cur - qty)
    _mark_intervention(state, True)
    return "🗑 你从池塘移除了 %d 个「%s」（不可逆）。" % (qty, name)


def _cmd_feed(state, args):
    env = state["env"]
    # feed [数量]，默认 1（item E1）
    qty = 1
    if args and args[0].lstrip("-").isdigit():
        qty = max(1, int(args[0]))
    # 累积效应：碎屑越多，未吃完的饲料越容易堆积恶化（item D3）
    det = env["detritus"]
    mult = 3.0 if det > 80 else (2.0 if det > 40 else 1.0)
    env["nutrients"] += 15 * qty
    env["detritus"] += 25 * qty * mult        # 碎屑增量按数量与累积倍率叠加
    state["populations"]["水蚤"] *= 1.05
    _mark_intervention(state, True)
    # 有鱼才写"鱼儿争食"，没鱼时只写残饵沉底（二轮#2）
    has_fish = any(state["populations"].get(f, 0) >= 1 for f in ("鲫鱼", "鲤鱼", "泥鳅"))
    if has_fish:
        lines = ["🍚 你向池塘投喂了 %d 份饲料。鱼儿争食，未吃完的沉入水底，慢慢腐解，水底多了一层沉淀。" % qty]
    else:
        lines = ["🍚 你向池塘投喂了 %d 份饲料。饲料沉入水底，慢慢腐解，水底多了一层沉淀。" % qty]
    if det > 80:
        # 碎屑 > 80：触发水质恶化描写
        lines.append("水已经发浑发腥了，沉底的腐泥一层压着一层。再这么喂下去，水迟早会臭。")
    elif det > 40:
        lines.append("水色比先前浊了些。喂下去的料有不少没被吃掉，正在水底慢慢沤着。")
    return "\n".join(lines)


def _cmd_clean(state, args):
    pop = state["populations"]
    # 记录清理前数量，便于在文案里写出具体代价（item D6）
    daphnia_before = _ipop(state, "水蚤")
    larva_before = _ipop(state, "孑孓")
    pop["水藻"] *= 0.4
    # 副作用：带走水蚤、孑孓和微生物
    pop["水蚤"] *= 0.5
    pop["孑孓"] *= 0.5
    pop["细菌"] *= 0.5
    state["env"]["detritus"] *= 0.7
    # 换水带走部分营养盐（item 8：营养盐不再只涨不降）
    state["env"]["nutrients"] *= 0.7
    state["env"]["turbidity"] = _clamp(state["env"]["turbidity"] - 0.2, 0, 1)
    # 换水带来新鲜含氧水，溶氧即时回升 1.5（二轮#4）
    state["env"]["dissolved_oxygen"] = min(state["env"]["dissolved_oxygen"] + 1.5, 12.0)
    # 换水降低疫病传播概率（持续 3 天）
    _chain_set(state, "sanitized", 3)
    _mark_intervention(state, True)
    lines = ["🧹 你捞走水藻，换了水。池水清澈起来，但许多微小的生命也随着水流走了。"]
    losses = []
    d_lost = daphnia_before - _ipop(state, "水蚤")
    l_lost = larva_before - _ipop(state, "孑孓")
    if d_lost > 0:
        losses.append("约 %d 只水蚤" % d_lost)
    if l_lost > 0:
        losses.append("约 %d 只孑孓" % l_lost)
    if losses:
        lines.append("清理带走了" + "和".join(losses) + "。")
    return "\n".join(lines)


def _cmd_crack(state, args):
    """凿冰洞：冬季冰封时凿穿冰面，恢复气体交换（ice_hole 持续 3 天，每天溶氧 +0.3）。

    碎屑 5/次，每冬最多 3 次（春季重置）；第 3 次额外降水温 0.5；
    效果延迟 2~3 天在后续 observe/gaze 中体现。
    """
    f = state["flags"]
    env = state["env"]
    if state["season"] != "冬" or not f.get("ice_on"):
        return "现在不需要凿冰。"
    if f.get("crack_count", 0) >= 3:
        return "冰面已经凿得够多了，再凿水温会降得更低。"
    if env["detritus"] < 5:
        return "碎屑不足。"
    r = rng_from(state)
    env["detritus"] -= 5
    f["crack_count"] = f.get("crack_count", 0) + 1
    _chain_set(state, "ice_hole", 3)
    lines = ["🧊 " + _gaze_pick(r, CRACK_ACTION["op"])]
    if f["crack_count"] >= 3:
        env["water_temp"] = _clamp(env["water_temp"] - 0.5, 0.0, 42.0)
    # 延迟反馈：2~3 天后在 observe/gaze 末尾追加一句一次性描写
    f["crack_hint_due"] = state["turn"] + r.randint(2, 3)
    f["crack_hint_idx"] = r.randint(0, len(CRACK_ACTION["hint"]) - 1)
    f["crack_hint_pending"] = True
    _mark_intervention(state, True)
    commit_rng(state, r)
    return "\n".join(lines)


def _cmd_shelter(state, args):
    """落叶床：冬季在岸边堆落叶为底栖越冬者保暖。

    碎屑 ≥20（消耗 20），每冬一次（春季重置）；chain "shelter" 持续到冬季结束，
    4~5 天后生效，使田螺/水蚯蚓/泥鳅冬季死亡率 -30%；效果在 _season_death_factor 检查。
    """
    f = state["flags"]
    env = state["env"]
    if state["season"] != "冬":
        return "现在不需要落叶床。"
    if f.get("shelter_used"):
        return "今冬已经铺过落叶床了。"
    if env["detritus"] < 20:
        return "碎屑不足，至少需要 20。"
