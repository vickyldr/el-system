def _chronicle(state, text, key=True):
    """向年鉴追加一条时间线记录。

    key=True 的记录同时进入 key_chronicle（关键事件：解锁/归零/定居者来去/灾害/
    决策/季节更替），供 export lite 保留；key=False 为日常记录（如定居者食物预警），
    只进完整年鉴。绝大多数 _chronicle 调用本就是关键事件，故默认 key=True。
    """
    line = "%s 第%d天：%s" % (state["season"], state["turn"], text)
    state["chronicle"].append(line)
    if key:
        state.setdefault("key_chronicle", []).append(line)


def _folio_bump(state, book, key, note=None):
    """访客志/事件志计数与影响描述累计。"""
    d = state["folio"][book].setdefault(key, {"count": 0, "notes": []})
    d["count"] += 1
    if note and note not in d["notes"]:
        d["notes"].append(note)


def _update_max_seen(state):
    """刷新历史最大值字典（种群 + 有机碎屑），供解锁制判定。"""
    ms = state["max_seen"]
    pop = state["populations"]
    for name in SPECIES:
        v = pop.get(name, 0)
        if v > ms.get(name, 0):
            ms[name] = v
    det = state["env"]["detritus"]
    if det > ms.get("有机碎屑", 0):
        ms["有机碎屑"] = det


def _check_discovery(state, events):
    """解锁制：生态达成条件时自动解锁物种并入册。

    解锁冷却：两次解锁至少间隔 5 天，且每回合最多解锁一种，避免一口气弹出一串。
    """
    if state["turn"] - state.get("last_unlock_turn", -999) < 5:
        return
    for name, cond, narrative in DISCOVERY_RULES:
        if name in state["unlocked_species"]:
            continue
        try:
            ok = cond(state)
        except Exception:
            ok = False
        if ok:
            state["unlocked_species"].append(name)
            state["last_unlock_turn"] = state["turn"]
            events.append("discover:%s——造物名册更新：【%s】" % (narrative, name))
            _chronicle(state, "%s——造物名册更新：%s" % (narrative, name))
            # 物种志首次解锁登记
            sp = state["folio"]["species"].setdefault(name, {})
            sp.setdefault("first_day", state["turn"])
            sp.setdefault("first_season", state["season"])
            sp.setdefault("extinct_count", 0)
            sp.setdefault("alive", state["populations"].get(name, 0) >= 1)
            return  # 一次只解锁一种，保证 5 天间隔


def _folio_update_species(state):
    """物种志：跟踪已解锁物种的存活/归零次数（历史最大值由 max_seen 提供）。"""
    cod = state["folio"]["species"]
    pop = state["populations"]
    for name in state["unlocked_species"]:
        e = cod.get(name)
        if e is None:
            e = cod[name] = {"first_day": state["turn"], "first_season": state["season"],
                             "extinct_count": 0, "alive": pop.get(name, 0) >= 1}
        alive = _is_present(state, name)
        if e.get("alive") and not alive:
            e["extinct_count"] = e.get("extinct_count", 0) + 1
            # 季节性自然归零（蜕变/冬枯/越冬）不触发冷却与重新引入提示
            seasonal = _is_seasonal_zero(state, name)
            if not seasonal:
                # 归零时间：用于 summon 的 15 天归零冷却
                state.setdefault("extinct_turn", {})[name] = state["turn"]
            # 归零记录旁追加一句回忆描写（item 31）+ summon 重新引入提示
            memory = EXTINCT_MEMORY.get(name)
            hint = "" if seasonal else " 它已经从池塘里消失了。如果条件合适，你可以用 summon 重新引入。"
            if memory:
                _chronicle(state, "%s 的身影再也看不到了。%s%s" % (name, memory, hint))
            else:
                _chronicle(state, "%s 的身影再也看不到了。%s" % (name, hint.lstrip()))
        e["alive"] = alive


def _natural_recovery(state, events, r):
    """物种归零后，生存条件重新满足时每天小概率自然冒出少量个体。

    条件：已解锁、非隐藏、当前种群为 0、且非"对应季节的季节性归零"；
    食物来源至少有一种 pop>0（生产者不检查食物）。满足时每天 1% 概率恢复 2-5 个体。
    不受 summon 的 15 天归零冷却限制。
    """
    pop = state["populations"]
    unlocked = state.get("unlocked_species", [])
    for name in RESIDENT_SPECIES:
        if name not in unlocked or pop.get(name, 0) >= 1:
            continue
        if _is_seasonal_zero(state, name):
            continue
        sp = SPECIES[name]
        if sp.get("trophic") != "producer":
            foods = sp.get("food_sources", [])
            if "飞虫" not in foods and not any(pop.get(f, 0) > 0 for f in foods):
                continue
        if r.random() < 0.01:
            pop[name] = pop.get(name, 0) + r.randint(2, 5)
            text = RECOVERY_TEXT.get(name, "%s 又重新出现在池塘里了。" % name)
            events.append("recovery:" + text)
            _chronicle(state, "%s 自然恢复" % name)


