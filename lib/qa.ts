// 挑一题：避开最近问过的 id；都问遍了就允许重复（从全集随机）。
export function pickQuestion(recentIds: number[] = []): QaQuestion {
  const fresh = QA_QUESTIONS.filter((x) => !recentIds.includes(x.id));
  const pool = fresh.length ? fresh : QA_QUESTIONS;
  return pool[Math.floor(Math.random() * pool.length)];
}

export function questionById(id: number): QaQuestion | undefined {
  return QA_QUESTIONS.find((x) => x.id === id);
}
