    frags = []
    disasters = [w for w in ("暴雨", "热浪", "干旱", "洪水", "水华", "翻塘", "寒潮")
                 if any(w in e for e in yr)]
    if disasters:
        frags.append("池塘挺过了" + "、".join(dict.fromkeys(disasters)))
    discovered = []
    for e in yr:
        discovered += re.findall(r"造物名册更新：([一-龥]+)", e)
