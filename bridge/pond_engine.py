def _classify_event(ev):
    """把内部事件标签解析为结构化信息：type / name（标题）/ effect（影响）。

    同时附带 icon、body（原文案），以及聚合所需的 into/count/subject 字段。
    不改动事件字符串本身，仅作只读解析。
    """
    if ":" in ev:
        tag, body = ev.split(":", 1)
    else:
        tag, body = "", ev
    meta = {
        "tag": tag, "icon": EVENT_ICONS.get(tag, "·"), "body": body,
        "type": tag or "info", "name": "", "effect": "",
        "into": None, "count": 0, "subject": None,
    }

    if tag == "season":
        name = "时序流转"
        for s, v in SEASON_ENV.items():
            if v["desc"] == body:
                name = "入" + s
                break
        else:
            # 季节性自动事件文案（蛙醒/睡莲抽芽/芦苇枯黄/睡莲开花等）
            for k, txt in SEASON_TEXT.items():
                if txt == body:
                    name = k
                    break
        meta["name"] = name
    elif tag == "lifecycle":
        m = re.search(r"(\d+)\s*只(.+?)悄然变了形态，化作(.+)", body)
        if m:
            meta["count"] = int(m.group(1))
            meta["into"] = m.group(3)
            meta["name"] = m.group(2) + "蜕变"
            meta["effect"] = "%s +%d" % (m.group(3), meta["count"])
        else:
            meta["name"] = "蜕变"
    elif tag == "spawn":
        if "蜻蜓" in body:
            meta["name"] = "蜻蜓产卵"
            meta["into"] = "蜻蜓幼虫"
            m = re.search(r"(\d+)\s*只幼虫", body)
            if m:
                meta["count"] = int(m.group(1))
                meta["effect"] = "蜻蜓幼虫 +%d" % meta["count"]
        else:
            meta["name"] = "青蛙产卵"
            meta["into"] = "蝌蚪"
            m = re.search(r"(\d+)\s*只蝌蚪", body)
            if m:
                meta["count"] = int(m.group(1))
                meta["effect"] = "蝌蚪 +%d" % meta["count"]
    elif tag == "discover":
        m = re.search(r"【(.+?)】", body)
        sp = m.group(1) if m else ""
        meta["name"] = "新物种解锁"
        meta["effect"] = ("解锁「%s」" % sp) if sp else "解锁新物种"
    elif tag == "settler":
        meta["name"] = "流浪乌龟离开" if "乌龟" in body else "定居者离开"
        meta["effect"] = "离开池塘"
    elif tag == "settler_hunt":
        who = next((n for n in ("翠鸟", "苍鹭") if n in body), "定居者")
        meta["name"] = who + "捕食"
        meta["effect"] = "捕食成功"
    elif tag == "settler_miss":
        who = next((n for n in ("翠鸟", "苍鹭") if n in body), "定居者")
        meta["name"] = who + "扑空"
        meta["effect"] = "扑空"
    elif tag == "settler_leave":
        who = next((n for n in SETTLER_TYPES if n in body), "定居者")
        meta["name"] = who + "离开"
        meta["effect"] = "离开池塘"
    elif tag == "settler_birth":
        who = next((n for n in SETTLER_TYPES if n in body), "定居者")
        meta["name"] = who + "繁殖"
        meta["effect"] = "添了新一代"
    elif tag == "settler_grown":
        who = next((n for n in SETTLER_TYPES if n in body), "定居者")
        meta["name"] = who + "成年"
        meta["effect"] = "幼体长成"
    elif tag == "achievement":
        m = re.search(r"【(.+?)】", body)
        meta["name"] = m.group(1) if m else "成就"
        meta["effect"] = "解锁成就"
    elif tag == "report":
        meta["name"] = "年终生态简报"
        meta["effect"] = "一年回顾"
    elif tag == "weather":
        meta["name"] = "天气"
        meta["effect"] = ""
    elif tag == "disease":
        meta["name"] = "疫病"
        meta["effect"] = "疫病"
    elif tag == "recovery":
        meta["name"] = "自然恢复"
        meta["effect"] = "归零物种重现"
    elif tag in ("choice", "choice_auto"):
        meta["name"] = _choice_title_from_text(body)
        meta["effect"] = "等待抉择" if tag == "choice" else "超时自动结算"
    elif tag in ("visitor", "legend", "disaster", "crisis"):
        for frag, title, etype, eff in _VISITOR_TABLE:
            if frag in body:
                meta["name"] = title
                meta["type"] = etype
                if title.endswith("来访"):
                    meta["subject"] = title[:-2]
                if frag == "翠蓝色":
                    fm = re.search(r"(\d+)\s*条鲫鱼", body)
                    meta["effect"] = "鲫鱼 -%s" % (fm.group(1) if fm else "?")
                else:
                    meta["effect"] = eff or ""
                break
        else:
            # 表中未命中：尝试从正文里识别访客主体（含扑空专属文案）
            sub = next((v for v in ("翠鸟", "苍鹭", "池鹭", "白鹭", "流浪猫")
                        if v in body), None)
            if sub and tag == "visitor":
                meta["name"] = sub + "来访"
                meta["subject"] = sub
                meta["effect"] = "扑空" if ("没有" in body or "空" in body
                                            or "什么也" in body) else ""
            else:
                meta["name"] = "访客来访" if tag == "visitor" else "异象"
    else:
        meta["name"] = "事件"
    return meta


def _render_events(events):
    """把内部事件渲染为：图标【事件名称】+ 换行 + 原文案。"""
    lines = []
    for ev in events:
        meta = _classify_event(ev)
        # 成就文案自带 🏆【名称】 格式，直接展示，避免双重标题
        if meta["tag"] == "achievement":
            lines.append(meta["body"])
            continue
        lines.append("%s【%s】\n%s" % (meta["icon"], meta["name"], meta["body"]))
    return lines


def _observe_ambient(state):
    """observe 的动态主描写（item 15/21）。

    分两段独立输出：
      环境段——灾害进行中 > 危机状态（低溶氧）> 高生物多样性
               > 高浮萍 > 高营养盐 > 高碎屑 > 默认（优先级取其一）。
      定居者段——若当前有定居者，追加一段 settler 描写。
    两段之间空一行；没有定居者就只输出环境段。
    """
    env = state["env"]
    pop = state["populations"]
    aw = state.get("active_weather")
    if aw:
        environ = _pick_t(state, WEATHER_ONGOING[aw["kind"]])
    else:
        if env["dissolved_oxygen"] < 4.0:
            cat = "low_do"
        elif len([n for n in RESIDENT_SPECIES
                  if n in _unlocked_set(state) and pop.get(n, 0) >= 1]) > 10:
            cat = "biodiverse"
        elif _surface_cover(state) > 0.7:
            cat = "high_cover"
        elif env["nutrients"] > 80:
            cat = "high_nutrients"
        elif env["detritus"] > 60:
            cat = "high_detritus"
        else:
            cat = "default"
        # 依赖物种不在场的文案会被过滤；全被过滤时降级到 default
        environ = (_pick_ambient(state, OBSERVE_AMBIENT[cat])
                   or _pick_ambient(state, OBSERVE_AMBIENT["default"])
                   or OBSERVE_AMBIENT["default"][0])
    if state.get("settlers"):
        # 定居者段只输出当前实际在场的定居者对应文案
        settler_txt = _pick_ambient(state, OBSERVE_AMBIENT["settler"])
        if settler_txt:
            return environ + "\n\n" + settler_txt
    return environ


def _settler_warn_lines(state):
    """定居者食物不足预警：health<0.5 轻预警 / <0.3 重预警（observe 中显示）。"""
    out = []
    for s in state.get("settlers", []):
        h = s["health"]
        name = s["name"]
        if h < 0.3 and name in SETTLER_WARN_HEAVY:
            out.append("· " + _pick_t(state, SETTLER_WARN_HEAVY[name]))
        elif h < 0.5 and name in SETTLER_WARN_LIGHT:
            out.append("· " + _pick_t(state, SETTLER_WARN_LIGHT[name]))
    return out


def _settler_prey(name, s):
    """定居者的猎物/食物物种列表（剔除环境来源如有机碎屑）。"""
    cfg = SETTLER_TYPES.get(name, {})
    if cfg.get("hunter"):
        return list(cfg["hunter"]["prey"])
    food = s.get("daily_food") or cfg.get("daily_food", {})
    return [k for k in food if k != "有机碎屑"]


def _settler_prey_gone_lines(state):
    """定居者猎物全部归零时的一次性预警（补一·2），猎物恢复后清除标记。"""
    flags = state["flags"]
    pop = state["populations"]
    out = []
    for s in state.get("settlers", []):
        name = s["name"]
        if name not in SETTLER_PREY_GONE:
            continue
        prey = _settler_prey(name, s)
        if not prey:
            continue
        all_gone = all(pop.get(p, 0) < 1 for p in prey)
        flag = "prey_gone_%s" % name
        if all_gone:
            if not flags.get(flag):
                out.append("· " + SETTLER_PREY_GONE[name])
                flags[flag] = True
        elif flags.get(flag):
            flags[flag] = False
    return out


def _season_omen_line(state):
    """换季前两天（每季第 29-30 天）追加一句季节预兆（补一·5）。"""
    turn = state["turn"]
    if turn <= 0 or (turn % SEASON_LEN) not in (28, 29):
        return None
    omens = SEASON_OMEN.get(state["season"])
    if not omens:
        return None
    return "· " + _pick_t(state, omens)


def _water_degrade_lines(state):
    """水质恶化的简短叙事反馈（item 16），与 gaze 环境描写对齐但更短。"""
    env = state["env"]
